      <div class="explications_etapes">
        <h2>Etape 8 : Le Conditionnement</h2>
        <p>Enfin, la bière est prête pour l'étape finale : le conditionnement. Elle est transférée dans des fûts, des bouteilles ou des canettes, prête à être distribuée et dégustée.</p>
